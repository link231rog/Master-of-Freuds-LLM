def build_conversation(rng):
    """Build a single user-assistant conversation."""
    concern = rng.choice(USER_CONCERNS)
    empathy = rng.choice(EMPATHY)
    insight = rng.choice(INSIGHTS)
    advice = rng.choice(ADVICE)

    # Vary structure
    style = rng.random()
    if style < 0.3:
        response = f"{empathy}\n\n{insight}\n\n{advice}"
    elif style < 0.5:
        response = f"{empathy}\n\n{advice}"
    elif style < 0.7:
        response = f"{empathy}\n\n{insight}"
    else:
        closings = ["你觉得呢？", "下次可以试试看。", "慢慢来，不着急。", "你说呢？"]
        response = f"{empathy}\n\n{insight}\n\n{advice}\n\n{rng.choice(closings)}"

    return {
        "messages": [
            {"role": "user", "content": concern},
            {"role": "assistant", "content": response},
        ]
    }


# Generate
rng = random.Random(42)
N_TARGET = 5000
examples = []
seen = set()

for _ in range(N_TARGET * 5):
    if len(examples) >= N_TARGET:
        break
    conv = build_conversation(rng)
    key = conv["messages"][1]["content"][:80]
    if key not in seen:
        seen.add(key)
        examples.append(conv)

rng.shuffle(examples)
n_dev = max(1, int(len(examples) * 0.02))
dev, train = examples[:n_dev], examples[n_dev:]

for split, data in [("train", train), ("dev", dev)]:
    path = f"{OUT_DIR}/qwen_psych_{split}.jsonl"
    with open(path, "w", encoding="utf-8") as f:
        for ex in data:
            f.write(json.dumps(ex, ensure_ascii=False) + "\n")
    print(f"{path}: {len(data)} examples")

