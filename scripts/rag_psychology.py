def retrieve_theory(query: str, top_k: int = 2):
    """Simple keyword matching to find relevant theories."""
    query_words = set(query.lower())
    scored = []
    for t in THEORY_DB:
        score = sum(1 for kw in t["keywords"] if kw in query)
        if score > 0:
            scored.append((score, t))
    scored.sort(key=lambda x: -x[0])
    if not scored:
        return []
    return [t for _, t in scored[:top_k]]


def build_prompt_with_theory(query: str):
    """Build a prompt with matched theory context."""
    theories = retrieve_theory(query)
    if not theories:
        return None, query  # No theory matched, use original query

    theory_text = ""
    for t in theories:
        theory_text += f"\n- {t['name']}（{t['author']}）：{t['desc']}。{t['advice']}"

    enhanced_query = f"请用「{theories[0]['name']}」来分析：{query}"

    if len(theories) > 1:
        enhanced_query = f"请用「{theories[0]['name']}」和「{theories[1]['name']}」来分析：{query}"

    return theories, enhanced_query
